import Foundation
import UIKit

extension UIColor {
    struct AppColor {
        struct Red {
            static let redDark = UIColor(hex: 0x4f0e15)
            static let red = UIColor(hex: 0x7a2c33)
            static let redLight = UIColor(hex: 0xc64853)
            static let redUltraLight = UIColor(hex: 0xef8f98)
            static let redUltraWhite = UIColor(hex: 0xffe1e4)
        }
        
        struct Gray {
            static let grayLight = UIColor(hex: 0xf5f5f5)
        }
    }
}

